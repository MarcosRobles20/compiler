    private void btn_ExaminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ExaminarActionPerformed
        JFileChooser fileChooser = new JFileChooser();

        // Mostrar el cuadro de diálogo de selección de archivo
        int resultado = fileChooser.showOpenDialog(this);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivoSeleccionado = fileChooser.getSelectedFile();
            try {
                BufferedReader br = new BufferedReader(new FileReader(archivoSeleccionado));
                String linea;
                txt_Codigo.setText(""); // Limpiar el JTextArea antes de agregar el nuevo contenido

                // Leer el contenido del archivo y agregarlo al JTextArea
                while ((linea = br.readLine()) != null) {
                    txt_Codigo.append(linea + "\n");
                }

                br.close();
            } catch (IOException ex) {
            }
        }

    }//GEN-LAST:event_btn_ExaminarActionPerformed

