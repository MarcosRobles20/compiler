    private void guardarTextoEnArchivo(File selectedFile, String texto) {
        try {
            BufferedWriter writer = new BufferedWriter(new FileWriter(selectedFile));
            writer.write(texto);
            writer.close();
            JOptionPane.showMessageDialog(null, "Contenido guardado en el archivo correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error al guardar el archivo.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
